def get_all_chapters():
    """Return all book chapters"""
    return BOOK_CHAPTERS


def get_chapter_by_id(chapter_id: int):
    """Get specific chapter by ID"""
    for chapter in BOOK_CHAPTERS:
        if chapter['id'] == chapter_id:
            return chapter
    return None


def search_chapters_by_keyword(keyword: str):
    """Simple keyword search across all chapters"""
    keyword_lower = keyword.lower()
    results = []
    
    for chapter in BOOK_CHAPTERS:
        if (keyword_lower in chapter['title'].lower() or 
            keyword_lower in chapter['content'].lower()):
            results.append(chapter)
    
    return results